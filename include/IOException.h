// Constructors

inline System::IO::IOException::IOException(const std::type_info& typeinfo, const std::string& methodName, const std::string& message) noexcept
	: RuntimeException{ typeinfo, methodName, message }
{
}

inline System::IO::IOException::IOException(const std::string& methodName, const std::string& message) noexcept
	: RuntimeException{ methodName, message }
{
}

