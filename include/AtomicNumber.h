// Methods

inline std::size_t ChemToolkit::Generic::AtomicNumber::getHashCode() const
{
	return toElementSymbol().getHashCode();
}

inline std::string ChemToolkit::Generic::AtomicNumber::toHashString() const noexcept
{
	return std::to_string(_number);
}

