// **********************************************************************************************************************************************************************************************************************************************************************************************
// Unary operators

inline ChemToolkit::Generic::AtomicNumber& ChemToolkit::Generic::AtomicNumber::operator++()
{
	if (isValid((_number + 1)))
	{
		++_number;
		return (*this);
	}

	else
		throw System::ExceptionServices::InvalidOperationException{ typeid(*this), "operator++", "Could not increase atomic number." };
}

