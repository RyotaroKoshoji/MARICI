// **********************************************************************************************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************************************************************************************
// Methods

inline bool ChemToolkit::Crystallography::Symmetry::SiteSymmetrySymbol::isValid() const noexcept
{
	return !(_symbol.empty());
}

inline std::size_t ChemToolkit::Crystallography::Symmetry::SiteSymmetrySymbol::getHashCode() const noexcept
{
	return std::hash<std::string>{}(_symbol);
}

