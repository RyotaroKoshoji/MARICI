// Constructors

inline System::IO::FileNotFoundException::FileNotFoundException(const std::type_info& typeinfo, const std::string& methodName, const std::string& message) noexcept
	: IOException{ typeinfo, methodName, message }
{
}

inline System::IO::FileNotFoundException::FileNotFoundException(const std::string& methodName, const std::string& message) noexcept
	: IOException{ methodName, message }
{
}

