inline std::string System::ExceptionServices::ArgumentException::toString() const
{
	std::string texts;
	{
		texts = typeid(*this).name();
		texts += "\n";
		{
			if (!(_className.empty()))
			{
				texts += "Class name : ";
				texts += _className;
				texts += "\n";
			}
		}

		texts += "Method name: ";
		texts += _methodName;
		texts += "\n";
		texts += "Message    : ";
		texts += std::invalid_argument::what();
		texts += "\n";
	}

	return texts;
}

