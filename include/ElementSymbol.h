// **********************************************************************************************************************************************************************************************************************************************************************************************
// Private methods

inline bool ChemToolkit::Generic::ElementSymbol::isValid(const std::string& symbol) noexcept
{
	if (getAtomicNumber(symbol) == std::numeric_limits<size_type>::max())
		return false;
	else
		return true;
}

