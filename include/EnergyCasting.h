namespace MathToolkit
{
	namespace UnitConversion
	{
		namespace EnergyCasting
		{
			template <typename from, typename to>
			inline constexpr double cast(const double value)
			{
				return ((value * from::getUnitEnergyInSI()) / to::getUnitEnergyInSI());
			}

			template <typename from, typename to>
			inline constexpr double getScalingMultiplier()
			{
				return (from::getUnitEnergyInSI() / to::getUnitEnergyInSI());
			}
		}
	}
}

