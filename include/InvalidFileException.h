#define SYSTEM_IO_INVALIDFILEEXCEPTION_H

#include "IOException.h"


namespace System
{
	namespace IO
	{
		class InvalidFileException :public IOException
		{
// **********************************************************************************************************************************************************************************************************************************************************************************************
		// Constructors, destructor, and operators

		public:
			InvalidFileException(const std::type_info&, const std::string& methodName, const std::string& message) noexcept;
			InvalidFileException(const std::string& methodName, const std::string& message) noexcept;
			virtual ~InvalidFileException() = default;

			InvalidFileException(const InvalidFileException&) = default;
			InvalidFileException(InvalidFileException&&) noexcept = default;
			InvalidFileException& operator=(const InvalidFileException&) = default;
			InvalidFileException& operator=(InvalidFileException&&) noexcept = default;

		// Constructors, destructor, and operators
// **********************************************************************************************************************************************************************************************************************************************************************************************

		private:
			InvalidFileException() = delete;
		};
	}
}

