#define SYSTEM_IO_DIRECTORYNOTFOUNDEXCEPTION_H

#include "IOException.h"


namespace System
{
	namespace IO
	{
		class DirectoryNotFoundException :public IOException
		{
// **********************************************************************************************************************************************************************************************************************************************************************************************
		// Constructors, destructor, and operators

		public:
			DirectoryNotFoundException(const std::type_info&, const std::string& methodName, const std::string& message) noexcept;
			DirectoryNotFoundException(const std::string& methodName, const std::string& message) noexcept;
			virtual ~DirectoryNotFoundException() = default;

			DirectoryNotFoundException(const DirectoryNotFoundException&) = default;
			DirectoryNotFoundException(DirectoryNotFoundException&&) noexcept = default;
			DirectoryNotFoundException& operator=(const DirectoryNotFoundException&) = default;
			DirectoryNotFoundException& operator=(DirectoryNotFoundException&&) noexcept = default;

		// Constructors, destructor, and operators
// **********************************************************************************************************************************************************************************************************************************************************************************************

		private:
			DirectoryNotFoundException() noexcept = delete;
		};
	}
}

