// Constructors

ElementSymbol::ElementSymbol() noexcept
	: _symbol{}
{
}

ElementSymbol::ElementSymbol(const std::string& symbol)
	: _symbol{ symbol }
{
	if (!(isValid(_symbol)))
		throw System::ExceptionServices::ArgumentOutOfRangeException{ typeid(*this), "constructor", "Element symbol is out of definitions." };
}

ElementSymbol::ElementSymbol(std::string&& symbol)
	: _symbol{ std::move(symbol) }
{
	if (!(isValid(_symbol)))
		throw System::ExceptionServices::ArgumentOutOfRangeException{ typeid(*this), "constructor", "Element symbol is out of definitions." };
}

ElementSymbol::ElementSymbol(const size_type number)
	: _symbol{ AtomicNumber{ number }.toElementSymbol() }
{
}

ElementSymbol::ElementSymbol(const std::size_t number)
	: _symbol{ AtomicNumber{ number }.toElementSymbol() }
{
}

ElementSymbol::ElementSymbol(const int number)
	: _symbol{ AtomicNumber{ number }.toElementSymbol() }
{
}

ElementSymbol::ElementSymbol(const AtomicNumber atomicNumber)
	: _symbol{ atomicNumber.toElementSymbol() }
{
}

