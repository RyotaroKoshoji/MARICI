std::string DateTime::toTimeString(const std::chrono::time_point<std::chrono::system_clock>& timePoint)
{
	/*
	std::stringstream timeStream;
	timeStream << std::chrono::zoned_time(std::chrono::current_zone(), timePoint);
	return timeStream.str();
	*/


	std::string timeString;
	{
		std::time_t timeT = std::chrono::system_clock::to_time_t(timePoint);
		const std::tm* localTime = std::localtime(&timeT);
		
		std::stringstream timeStream;
		timeStream << std::put_time(localTime, "%c");

		timeString = timeStream.str();
	}
	return timeString;
}

