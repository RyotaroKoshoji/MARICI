// Constructors

SphericalAtom::SphericalAtom() noexcept
	: _cartesianCoordinate{ 0.0, 0.0, 0.0 }
	, _appliedForce{ 0.0, 0.0, 0.0 }
	, _covalentRadius{}
	, _ionicRadius{}
	, _ionicRepulsionRadius{}
{
}

SphericalAtom::SphericalAtom(const NumericalVector& coordinate) noexcept
	: _cartesianCoordinate{ coordinate }
	, _appliedForce{ 0.0, 0.0, 0.0 }
	, _covalentRadius{}
	, _ionicRadius{}
	, _ionicRepulsionRadius{}
{
}

SphericalAtom::SphericalAtom(const AtomicNumber number, const charge_type charge)
	: _cartesianCoordinate{ 0.0, 0.0, 0.0 }
	, _appliedForce{ 0.0, 0.0, 0.0 }
	, _covalentRadius{ number, charge }
	, _ionicRadius{ number, charge }
	, _ionicRepulsionRadius{ number, charge }
{
}

SphericalAtom::SphericalAtom(const AtomicNumber number, const charge_type charge, const NumericalVector& coordinate)
	: _cartesianCoordinate{ coordinate }
	, _appliedForce{ 0.0, 0.0, 0.0 }
	, _covalentRadius{ number, charge }
	, _ionicRadius{ number, charge }
	, _ionicRepulsionRadius{ number, charge }
{
}

SphericalAtom::SphericalAtom(const ConstrainingAtom& atom) noexcept
	: _cartesianCoordinate{ atom.cartesianCoordinate() }
	, _appliedForce{ 0.0, 0.0, 0.0 }
	, _covalentRadius{ atom.covalentRadius() }
	, _ionicRadius{ atom.ionicRadius() }
	, _ionicRepulsionRadius{ atom.ionicRepulsionRadius() }
{
}

