// Constructors

template <>
AtomicRadius<covalent_radius_tag>::AtomicRadius(const IonicAtomicNumber& ionicAtomicNumber)
	: _minimum{ AtomicRadiusDictionary::getMinimumCovalentRadius(ionicAtomicNumber) }
	, _maximum{ AtomicRadiusDictionary::getMaximumCovalentRadius(ionicAtomicNumber) }
{
}

template <>
AtomicRadius<ionic_radius_tag>::AtomicRadius(const IonicAtomicNumber& ionicAtomicNumber)
	: _minimum{ AtomicRadiusDictionary::getMinimumCovalentRadius(ionicAtomicNumber) }
	, _maximum{ AtomicRadiusDictionary::getMaximumCovalentRadius(ionicAtomicNumber) }
{
}

template <>
AtomicRadius<covalent_radius_tag>::AtomicRadius(const AtomicNumber number, const charge_type charge)
	: _minimum{ AtomicRadiusDictionary::getMinimumCovalentRadius(IonicAtomicNumber{ number, charge }) }
	, _maximum{ AtomicRadiusDictionary::getMaximumCovalentRadius(IonicAtomicNumber{ number, charge }) }
{
}

template <>
AtomicRadius<ionic_radius_tag>::AtomicRadius(const AtomicNumber number, const charge_type charge)
	: _minimum{ AtomicRadiusDictionary::getMinimumIonicRadius(IonicAtomicNumber{ number, charge }) }
	, _maximum{ AtomicRadiusDictionary::getMaximumIonicRadius(IonicAtomicNumber{ number, charge }) }
{
}


AtomicRadius<ionic_repulsion_radius_tag>::AtomicRadius() noexcept
	: _minimum{ 0.0 }
{
}

AtomicRadius<ionic_repulsion_radius_tag>::AtomicRadius(const double minimumRadius)
	: _minimum{ 0.0 }
{
	if (minimumRadius < 0.0)
		throw System::ExceptionServices::ArgumentOutOfRangeException{ typeid(*this), "constructor", "Minimum value is negative." };
	else
		_minimum = minimumRadius;
}

AtomicRadius<ionic_repulsion_radius_tag>::AtomicRadius(const IonicAtomicNumber& ionicAtomicNumber)
	: _minimum{ AtomicRadiusDictionary::getMinimumIonicRepulsionRadius(ionicAtomicNumber) }
{
}

AtomicRadius<ionic_repulsion_radius_tag>::AtomicRadius(const AtomicNumber number, const charge_type charge)
	: _minimum{ AtomicRadiusDictionary::getMinimumIonicRepulsionRadius(IonicAtomicNumber{ number, charge }) }
{
}

